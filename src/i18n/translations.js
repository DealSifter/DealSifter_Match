// ── Language system (mirrors the theme system pattern) ─────────────────────
const LANG_ALIAS = {
  en: 'en',
  'en-us': 'en',
  pt: 'pt',
  'pt-br': 'pt',
  es: 'es',
  'es-es': 'es',
};

const DISPLAY_LANG = {
  en: 'en-US',
  pt: 'pt-BR',
  es: 'es-ES',
};

const DEFAULT_LANG = { base: 'en', display: 'en-US' };

const normalizeLang = (lang) => {
  if (!lang || typeof lang !== 'string') return null;
  const key = lang.toLowerCase();
  const base = LANG_ALIAS[key] || LANG_ALIAS[key.split('-')[0]];
  if (!base) return null;
  return { base, display: DISPLAY_LANG[base] };
};

const initialLang = (() => {
  const saved = typeof localStorage !== 'undefined' ? localStorage.getItem('lang') : null;
  const fromSaved = normalizeLang(saved);
  if (fromSaved) return fromSaved;
  return DEFAULT_LANG;
})();

let currentLang = initialLang.display;
let currentLangBase = initialLang.base;
const langListenersByScope = new Map();

const getScopeKey = (scope) => (scope && typeof scope === 'string' ? scope : 'global');

const ensureScopeBucket = (scope) => {
  const key = getScopeKey(scope);
  if (!langListenersByScope.has(key)) {
    langListenersByScope.set(key, new Set());
  }
  return langListenersByScope.get(key);
};

export const getLang = () => currentLang;
export const getT = () => translations[currentLangBase] || translations.en;

// Language change notifier removed; `setLang` notifies listeners directly.

export const setLang = (lang) => {
  let sourceScope = 'global';
  if (typeof lang === 'object' && lang !== null) {
    sourceScope = getScopeKey(lang.sourceScope);
    lang = lang.value;
  }
  const normalized = normalizeLang(lang);
  if (!normalized || !translations[normalized.base]) return;
  if (normalized.display === currentLang && normalized.base === currentLangBase) return;
  currentLang = normalized.display;
  currentLangBase = normalized.base;
  if (typeof localStorage !== 'undefined') {
    localStorage.setItem('lang', currentLang);
  }

  const notified = new Set();
  const notifyBucket = (scope) => {
    const bucket = langListenersByScope.get(scope);
    if (!bucket) return;
    bucket.forEach((listener) => {
      if (notified.has(listener)) return;
      notified.add(listener);
      listener();
    });
  };

  // First pass: current page/module and global chrome update immediately.
  notifyBucket('global');
  if (sourceScope !== 'global') {
    notifyBucket(sourceScope);
  }

  const notifyDeferredScopes = () => {
    const run = () => {
      langListenersByScope.forEach((_, scope) => {
        if (scope === 'global' || scope === sourceScope) return;
        notifyBucket(scope);
      });
    };

    if (typeof startTransition === 'function') {
      startTransition(run);
      return;
    }

    run();
  };

  if (typeof requestAnimationFrame === 'function') {
    requestAnimationFrame(notifyDeferredScopes);
  } else {
    setTimeout(notifyDeferredScopes, 0);
  }
};

export const subscribeLang = (cb, scope = 'global') => {
  const bucket = ensureScopeBucket(scope);
  bucket.add(cb);
  return () => {
    bucket.delete(cb);
    if (bucket.size === 0) {
      langListenersByScope.delete(getScopeKey(scope));
    }
  };
};

export const useLang = (scope = 'global') => useSyncExternalStore(
  (cb) => subscribeLang(cb, scope),
  getLang,
  getLang,
);

export const useT = (scope = 'global') => {
  useSyncExternalStore((cb) => subscribeLang(cb, scope), getLang, getLang);
  return getT();
};
